# ELIF = Else IF Statement

nama = input("Siapa nama Anda?: ")

if nama == "udin":              # kondisi 1
    print("Cek DM din!")            # aksi True 1
elif nama == "ucup":            # kondisi 2
    print("Cek DM cup!")            # aksi True 2
elif nama == "darto":           # kondisi 3
    print("Cek DM to!")             # aksi True 3
else:
    print("Maaf, bukan Anda!")      # aksi false

print("Akhir dari program.")